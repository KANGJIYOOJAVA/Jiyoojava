public class Day20220419Home01 {

	public static void main(String[] args) {

		int sum = 0;
		int count = 0;
		
		for(int i=1;i<=100;i++) {
			
			if(i%2 ==0 && i%7 ==0) {
				count++;
				sum += i;
				System.out.print(i + " ");
				
			System.out.printf("1부터 %2d 까지의 합: %2d%n", i, sum);
			
			
	}
	}
	}
}

